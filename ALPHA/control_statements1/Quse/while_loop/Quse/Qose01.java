package Quse;

import java.util.Scanner;

public class Qose01 {
    public static void main(String[] args) {
        int n;
        int counter = 1;
        Scanner sc = new Scanner(System.in);
        n = sc.nextInt();
        while (counter <= n) {
            System.out.println(counter);
            counter++;

        }
    }
}
